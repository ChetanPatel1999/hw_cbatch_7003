// nested loop example
#include <stdio.h>
void main()
{
    int i, j;
    for (i = 1; i <= 3; i++) // i = 6
    {
        for (j = 1; j <= 2; j++) // j =4
        {
            printf("hello students\n");
        }
        printf("----------------------\n");
    }
}

