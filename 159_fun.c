//some built in function of math library
#include <stdio.h>
#include <math.h>
void main()
{
    printf("res = %.0f\n", pow(5, 3));
    printf("sqrt  = %.0f\n", sqrt(36));
    printf("sqrt  = %.0f\n", sqrt(64));
    printf("absolute value  = %d\n", abs(64));
    printf("absolute value  = %d\n", abs(-64));
    printf("floor value  = %.0f\n", floor(3.4));
    printf("ceil value  = %.0f\n", ceil(3.1));
}